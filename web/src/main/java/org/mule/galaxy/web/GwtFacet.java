package org.mule.galaxy.web;

public class GwtFacet {
    private String name;
    private String token;
    
    public String getName() {
        return name;
    }

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public void setName(String name) {
		this.name = name;
	}
}
